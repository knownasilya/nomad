// Spike for ADR-0010 Open question 6 (view/index layer: Hyperbee vs HyperDB).
// THROWAWAY — not wired into the app. The Hyperbee-view spike (spike-autobase-fs.mjs)
// already validated the blob model on a plain Hyperbee. This one answers the narrower,
// still-open part of Q6: does HYPERDB tolerate being an Autobase VIEW when Autobase
// RE-RUNS apply() on a reorg — i.e. are HyperDB insert/delete AND its SECONDARY-INDEX
// maintenance replay-safe and convergent, the way a keyed Hyperbee put/del is?
//
// It builds a HyperDB view (holepunchto/hyperdb) with a collection + a non-unique
// secondary index (generated by build-hyperdb-def.cjs — run automatically if absent),
// pairs two writers, then partitions them, writes concurrently on both, heals, and
// checks: (a) both views converge byte-identically after the reorg, (b) the secondary
// index is still correct and consistent on both sides, (c) a delete removes the record
// AND its index entry after replay.
//
// hyperdb is NOT a committed dependency (Q6 hasn't decided to adopt it). Install it
// transiently into app/node_modules before running, then run from the app dir so both the
// P2P deps and the generated def resolve:
//   cd ~/maintained/nomad/app && npm install --no-save hyperdb && node scripts/spike-hyperdb-view.mjs
//
// Exit 0 = HyperDB is a viable Autobase view (Q6 can pick HyperDB on its merits);
// Exit 1 = not proven replay-safe (Q6 should stay on a plain Hyperbee).

import os from 'os'
import path from 'path'
import fs from 'fs'
import { fileURLToPath, pathToFileURL } from 'url'
import { execFileSync } from 'child_process'
import Corestore from 'corestore'
import Autobase from 'autobase'
import HyperDB from 'hyperdb'
import b4a from 'b4a'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const DB_DIR = path.join(__dirname, 'hyperdb-spec', 'db')

// Generate the HyperDB definition on first run (codegen step — the "schema build" HyperDB
// requires). Deterministic, so regenerating is a no-op.
if (!fs.existsSync(path.join(DB_DIR, 'index.js'))) {
  console.log('generating hyperdb def (one-time codegen)…')
  execFileSync(process.execPath, [path.join(__dirname, 'build-hyperdb-def.cjs')], { stdio: 'inherit' })
}
const def = (await import(pathToFileURL(path.join(DB_DIR, 'index.js')).href)).default

const TMP = fs.mkdtempSync(path.join(os.tmpdir(), 'hdb-spike-'))
const results = []
const sleep = (ms) => new Promise((r) => setTimeout(r, ms))
function check (name, ok, detail = '') { results.push({ name, ok }); console.log(`  ${ok ? '✅ PASS' : '❌ FAIL'}  ${name}${detail ? ` — ${detail}` : ''}`) }
async function waitFor (fn, ms = 20000, every = 150) { const end = Date.now() + ms; while (Date.now() < end) { if (await fn()) return true; await sleep(every) } return false }

// --- HyperDB as the Autobase view ---------------------------------------------
// apply() stays a PURE, REPLAY-SAFE reducer: typed insert/delete + host.addWriter only,
// no side effects — the same invariant the shared Hyperbee reducer obeys. The open question
// is whether HyperDB's internal write + index bookkeeping is ALSO deterministic under replay.
function open (store) { return HyperDB.bee(store.get({ name: 'db' }), def, { autoUpdate: true }) }
async function apply (nodes, view, host) {
  for (const { value } of nodes) {
    if (!value) continue
    if (value.addWriter) { await host.addWriter(b4a.from(value.addWriter, 'hex'), { indexer: true }); continue }
    if (value.op === 'put') await view.insert('@fs/files', { path: value.path, body: value.body ?? '', tag: value.tag ?? '' })
    else if (value.op === 'del') await view.delete('@fs/files', { path: value.path })
  }
  await view.flush() // commit buffered inserts/deletes + secondary-index entries into the view bee
}

async function makeDevice (name, bootstrapKey = null) {
  const store = new Corestore(path.join(TMP, name)); await store.ready()
  const base = new Autobase(store, bootstrapKey, { open, apply, valueEncoding: 'json', ackInterval: 1000 })
  await base.ready()
  return { name, store, base }
}
function connect (a, b) { const s1 = a.store.replicate(true), s2 = b.store.replicate(false); s1.on('error', () => {}); s2.on('error', () => {}); s1.pipe(s2).pipe(s1); return () => { s1.destroy(); s2.destroy() } }
async function getFile (dev, p) { await dev.base.view.update(); return dev.base.view.get('@fs/files', { path: p }) }
async function byTag (dev, tag) { await dev.base.view.update(); const out = []; for await (const r of dev.base.view.find('@fs/files-by-tag', { gte: { tag }, lte: { tag } })) out.push(r.path); return out.sort() }
async function dump (dev) { await dev.base.view.update(); const out = []; for await (const r of dev.base.view.find('@fs/files', {})) out.push(`${r.path}|${r.body}|${r.tag}`); return out.sort().join('\n') }

async function main () {
  console.log(`\n=== ADR-0010 Q6 spike — HyperDB view under reorg — tmp ${TMP} ===\n`)

  const A = await makeDevice('A')
  await A.base.append({ op: 'put', path: '/index.json', body: '{"title":"hdb"}', tag: 'meta' })
  await A.base.update()
  check('A boots a HyperDB view + inserts a record', !!(await getFile(A, '/index.json')))

  const B = await makeDevice('B', A.base.key)
  let disconnect = connect(A, B)
  const bGotBoot = await waitFor(async () => { await B.base.update(); return !!(await getFile(B, '/index.json')) })
  check('B replicates A\'s HyperDB view', bGotBoot)

  await A.base.append({ addWriter: B.base.local.key.toString('hex') })
  await A.base.update()
  const bWritable = await waitFor(async () => { await A.base.update(); await B.base.update(); return B.base.writable })
  check('B becomes a writer', bWritable)

  // Seed tagged records so the secondary index has content to verify.
  await A.base.append({ op: 'put', path: '/a1.txt', body: 'a1', tag: 'red' })
  await A.base.append({ op: 'put', path: '/a2.txt', body: 'a2', tag: 'blue' })
  await A.base.update()
  await waitFor(async () => { await B.base.update(); return !!(await getFile(B, '/a2.txt')) })
  check('secondary index query works pre-reorg (tag=red)', JSON.stringify(await byTag(B, 'red')) === JSON.stringify(['/a1.txt']))

  // ---- Partition → concurrent writes → heal → reorg ----
  console.log('\n  partition → concurrent tagged writes on both writers → heal → reorg')
  disconnect(); await sleep(300)
  await A.base.append({ op: 'put', path: '/from-a.txt', body: 'A', tag: 'red' })   // both use tag 'red'
  await B.base.append({ op: 'put', path: '/from-b.txt', body: 'B', tag: 'red' })
  await A.base.update(); await B.base.update()
  disconnect = connect(A, B)

  const converged = await waitFor(async () => {
    await A.base.update(); await B.base.update()
    const da = await dump(A), db = await dump(B)
    return da === db && da.includes('/from-a.txt') && da.includes('/from-b.txt')
  }, 30000)
  check('both HyperDB views CONVERGE byte-identically after reorg', converged)

  // The crux: after apply() re-ran on the reorg, is the SECONDARY INDEX still correct?
  const redA = await byTag(A, 'red'), redB = await byTag(B, 'red')
  const expectedRed = ['/a1.txt', '/from-a.txt', '/from-b.txt']
  check('secondary index intact + consistent after reorg (tag=red)',
    JSON.stringify(redA) === JSON.stringify(expectedRed) && JSON.stringify(redB) === JSON.stringify(expectedRed),
    `A=${JSON.stringify(redA)} B=${JSON.stringify(redB)}`)

  // A delete must also remove its index entry, replay-safely.
  await A.base.append({ op: 'del', path: '/a1.txt' })
  await A.base.update()
  await waitFor(async () => { await B.base.update(); return !(await getFile(B, '/a1.txt')) })
  const afterDel = await byTag(B, 'red')
  check('delete removes the record AND its index entry after replay',
    JSON.stringify(afterDel) === JSON.stringify(['/from-a.txt', '/from-b.txt']), `got ${JSON.stringify(afterDel)}`)

  disconnect()
  await Promise.all([A.base.close(), B.base.close()])
}

main().then(() => {
  const failed = results.filter((r) => !r.ok)
  console.log(`\n=== ${results.length - failed.length}/${results.length} checks passed ===`)
  if (failed.length) console.log('FAILED:', failed.map((r) => r.name).join(' | '))
  console.log('Verdict:', failed.length
    ? 'HyperDB-as-view NOT proven replay-safe under reorg — Q6 leans Hyperbee'
    : 'HyperDB works as an Autobase view incl. secondary index under reorg — Q6 HyperDB is technically viable')
  fs.rmSync(TMP, { recursive: true, force: true })
  process.exit(failed.length ? 1 : 0)
}).catch((e) => { console.error('\nSPIKE CRASHED:', e.stack || e); fs.rmSync(TMP, { recursive: true, force: true }); process.exit(2) })
