#!/usr/bin/env node
// Generate TypeScript declarations for the walled.garden Zod schemas and write
// them as a JS module the editor feeds to the Monaco language service.
//
// Pipeline: Zod v4 schema -> z.toJSONSchema() -> a small JSON-Schema-to-TS
// converter -> `declare namespace WalledGarden { ... }`.
//
// Run as part of the build (see scripts/build.js) so the generated module exists
// before the editor bundle imports it. The output file is committed so the editor
// works without a build, and regenerated whenever the schemas change.

import { createRequire } from 'module';
import { fileURLToPath, pathToFileURL } from 'url';
import { spawnSync } from 'child_process';
import path from 'path';
import fs from 'fs';

// The walled.garden schemas are now TypeScript (app/lib/schemas/walled.garden/*.ts).
// We import them directly here via Node's type-stripping loader. Re-exec ourselves with
// the required flags on first entry so callers (scripts/build.js, or a bare
// `node scripts/gen-schema-dts.mjs`) don't need to know about them. Node 22 strips types
// but can't run enums/namespaces-with-runtime-semantics — the schema files are plain
// `z.object(...)` + `export type`, which is fully strippable.
if (!process.env.__WG_GEN_STRIP) {
  const r = spawnSync(
    process.execPath,
    ['--experimental-strip-types', '--no-warnings', fileURLToPath(import.meta.url)],
    { stdio: 'inherit', env: { ...process.env, __WG_GEN_STRIP: '1' } }
  );
  process.exit(r.status ?? 1);
}

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const appDir = path.resolve(__dirname, '../app');
const OUT_FILE = path.join(
  appDir,
  'userland/editor/js/types/schemas-dts.js'
);
const OUT_FILE_JSON = path.join(
  appDir,
  'userland/editor/js/types/schemas-json.js'
);

// Resolve zod + the schemas from the app's node_modules (zod lives under app/).
const appRequire = createRequire(path.join(appDir, 'package.json'));
const { z } = await import(pathToFileURL(appRequire.resolve('zod')).href);
const { SCHEMAS } = await import(
  pathToFileURL(
    path.join(appDir, 'lib/schemas/walled.garden/index.ts')
  ).href
);

// 'walled.garden/person' -> 'Person'
function interfaceName(type) {
  const last = type.split('/').pop();
  return last
    .split(/[-_.]/)
    .filter(Boolean)
    .map((s) => s[0].toUpperCase() + s.slice(1))
    .join('');
}

function tsType(schema, indent) {
  if (!schema || typeof schema !== 'object') return 'any';
  if (schema.const !== undefined) return JSON.stringify(schema.const);
  if (Array.isArray(schema.enum)) {
    return schema.enum.map((v) => JSON.stringify(v)).join(' | ');
  }
  if (Array.isArray(schema.anyOf)) {
    return schema.anyOf.map((s) => tsType(s, indent)).join(' | ');
  }
  switch (schema.type) {
    case 'string':
      return 'string';
    case 'number':
    case 'integer':
      return 'number';
    case 'boolean':
      return 'boolean';
    case 'null':
      return 'null';
    case 'array': {
      const item = tsType(schema.items, indent);
      return /[ |]/.test(item) ? `(${item})[]` : `${item}[]`;
    }
    case 'object':
      return objectType(schema, indent);
    default:
      return 'any';
  }
}

function objectType(schema, indent) {
  const props = schema.properties || {};
  const required = new Set(schema.required || []);
  const pad = '  '.repeat(indent + 1);
  const closePad = '  '.repeat(indent);
  const lines = Object.entries(props).map(([key, sub]) => {
    const optional = required.has(key) ? '' : '?';
    const safeKey = /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(key)
      ? key
      : JSON.stringify(key);
    return `${pad}${safeKey}${optional}: ${tsType(sub, indent + 1)};`;
  });
  if (!lines.length) return 'Record<string, never>';
  return `{\n${lines.join('\n')}\n${closePad}}`;
}

// Per-type JSON Schemas (reused for both the .d.ts and the JSON language service)
const jsonSchemas = Object.entries(SCHEMAS).map(([type, schema]) => ({
  type,
  name: interfaceName(type),
  json: z.toJSONSchema(schema, { target: 'draft-7' }),
}));

const interfaces = jsonSchemas.map(({ type, name, json }) => {
  const body = objectType(json, 1); // indent 1 => inside the namespace
  return `  /** Type for schema "${type}". */\n  interface ${name} ${body}`;
});

const dts = `declare namespace WalledGarden {
${interfaces.join('\n\n')}
}
`;

const fileContents = `// AUTO-GENERATED by scripts/gen-schema-dts.mjs from
// app/lib/schemas/walled.garden/*.ts — do not edit by hand.
// Fed to the Monaco language service so editor code gets schema types under the
// global \`WalledGarden\` namespace (e.g. WalledGarden.Person).

export const SCHEMAS_DTS = ${JSON.stringify(dts)};
`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, fileContents);
console.log(
  `gen-schema-dts: wrote ${Object.keys(SCHEMAS).length} schema types to ${path.relative(appDir, OUT_FILE)}`
);

// Drive manifest (index.json) properties — kept in sync with the docs at
// nomad.dev/content/docs/api/developers/index.json-manifest.md. A walled.garden
// record can be stored as a drive's index.json, where these fields are merged in
// alongside the record — so every record branch allows them (optional) to avoid
// additionalProperties:false flagging them.
const MANIFEST_PROPERTIES = {
  title: { type: 'string', description: 'Manifest: human-readable name for the drive' },
  description: { type: 'string', description: "Manifest: short description of the drive's contents" },
  type: { type: 'string', description: 'Manifest: type identifier (e.g. unwalled.garden/website)' },
  thumb: { type: 'string', description: 'Manifest: path within the drive to the thumbnail image' },
  author: {
    type: 'object',
    description: 'Manifest: information about the drive author',
    properties: {
      url: { type: 'string', description: "The author profile drive's hyper:// URL" },
    },
  },
  forkOf: { type: 'string', description: 'Manifest: hyper:// URL this drive was forked from' },
  links: {
    type: 'object',
    description: 'Manifest: map of named link arrays',
    additionalProperties: {
      type: 'array',
      items: {
        type: 'object',
        properties: { href: { type: 'string' } },
      },
    },
  },
  csp: { type: 'string', description: 'Manifest: Content-Security-Policy applied to pages served from the drive' },
  ai: {
    description: 'Manifest: opts the drive into beaker.ai — inline config or a pointer hyper:// URL',
    oneOf: [
      { type: 'string', description: "Pointer: delegate to another drive's AI config" },
      {
        type: 'object',
        description: 'Inline AI config',
        properties: { model: { type: 'string', description: 'Model identifier, e.g. llama3.2:3b' } },
      },
    ],
  },
  chatBubble: {
    type: 'boolean',
    description: 'Manifest: inject a floating AI chat bubble into every page on the drive (requires the ai field)',
  },
  web_root: { type: 'string', description: 'Manifest: directory to serve as the web root' },
  fallback_page: { type: 'string', description: 'Manifest: SPA fallback page path' },
};

const allTypes = jsonSchemas.map((s) => s.type);

// Combined JSON Schema for the Monaco JSON language service. Discriminates on the
// `type` field:
//  - a .json whose type matches a walled.garden schema gets that schema's
//    autocomplete + validation, with the drive-manifest fields merged in (so a
//    record stored as a drive's index.json validates — record + manifest merged;
//    the record's own fields win for overlaps like `links`).
//  - any other / typeless .json (a plain drive manifest, index.json, etc.) gets
//    the manifest fields. This is a separate branch gated on NOT being a
//    walled.garden record, so the manifest's `links` (object) never conflicts
//    with a record's `links` (array).
const combinedSchema = {
  $schema: 'http://json-schema.org/draft-07/schema#',
  title: 'walled.garden records & drive manifest',
  allOf: [
    ...jsonSchemas.map(({ type, json }) => ({
      if: { required: ['type'], properties: { type: { const: type } } },
      then: {
        ...json,
        // schema's own properties win (e.g. the `type` const, `links` array),
        // manifest fills the rest
        properties: { ...MANIFEST_PROPERTIES, ...(json.properties || {}) },
      },
    })),
    {
      // not a walled.garden record → treat as a drive manifest (permissive)
      if: {
        not: { required: ['type'], properties: { type: { enum: allTypes } } },
      },
      then: { type: 'object', properties: MANIFEST_PROPERTIES },
    },
  ],
};

const jsonContents = `// AUTO-GENERATED by scripts/gen-schema-dts.mjs from
// app/lib/schemas/walled.garden/*.ts — do not edit by hand.
// Registered with monaco.languages.json.jsonDefaults so a .json file whose "type"
// field matches a walled.garden schema gets that schema's autocomplete/validation.

export const WALLED_GARDEN_TYPES = ${JSON.stringify(allTypes)};
export const WALLED_GARDEN_JSON_SCHEMA = ${JSON.stringify(combinedSchema, null, 2)};
`;

fs.writeFileSync(OUT_FILE_JSON, jsonContents);
console.log(
  `gen-schema-dts: wrote combined JSON schema to ${path.relative(appDir, OUT_FILE_JSON)}`
);
